  public void cadEsportiva() throws CpfPeqException, CpfGrdException, IdadeGrdException, IdadePeqException{
    try{
      e = new Esportiva();
      
      e.setPlaca(tfPlaca.getText());
      e.setVelocidade_zero_cem(Integer.parseInt(tfVelocidade.getText()));
      e.setCilindradas(Integer.parseInt(tfCilindrada.getText()));
      e.setChassi(tfChassi.getText());
      e.setCor(tfCor.getText());
      e.setCpf(Integer.parseInt(tfCpf.getText()));
      e.setIdade(Integer.parseInt(tfIdade.getText()));
      e.setSistema_freio(tfSistemadeFreio.getText());
      e.setMarca(tfMarca.getText());
      e.setModelo(tfModelo.getText());
      e.setNome(tfNome.getText());
      e.setPeso(Integer.parseInt(tfPeso.getText()));
      e.setValor(Integer.parseInt(tfValor.getText()));
      
      e = Banco.getBancoMotors().cadEsportiva(e);
      
      if(e != null){
        JOptionPane.showMessageDialog(null, "Moto Cadastrada!", "Aviso", 1);
      }
      else{
        JOptionPane.showMessageDialog(null, "Moto ja cadastrada!", "Aviso", 0);
      }
      
    }
    catch(NumberFormatException e) {
      JOptionPane.showMessageDialog(null, "+Capacidade do tanque\n+Cilindradas\n+CPF\n+Idade\n+Peso\n+Valor\ndevem ser valores inteiros", "ATENCAO",2);
      limpar();
    }
  }
  
  public void consEsportiva(){
    e = new Esportiva();
    e.setPlaca(tfPlaca.getText());
    e = Banco.getBancoMotors().consEsportiva(e);
    
    if(e != null){
      tfPlaca.setText(e.getPlaca());
      tfVelocidade.setText(Integer.toString((int) e.getVelocidade_zero_cem()));
      tfCilindrada.setText(Integer.toString(e.getCilindradas()));
      tfChassi.setText(e.getChassi());
      tfCor.setText(e.getCor());
      tfCpf.setText(Integer.toString(e.getCpf()));
      tfIdade.setText(Integer.toString(e.getIdade()));
      tfSistemadeFreio.setText(e.getSistema_freio());
      tfMarca.setText(e.getMarca());
      tfModelo.setText(e.getModelo());
      tfNome.setText(e.getNome());
      tfPeso.setText(Integer.toString((int) e.getPeso()));
      tfValor.setText(Integer.toString((int) e.getValor()));
    }
    else{
      JOptionPane.showMessageDialog(null,"CPF nao cadastrado", "Consulta de Moto",0);
    }
  }
  
